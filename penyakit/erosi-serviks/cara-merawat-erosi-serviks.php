            <h6><b>Bagaimana Erosi Serviks Diobati?</b></h6>

            <p>
              Biasanya erosi serviks tidak menyebabkan gejala dan tidak memerlukan Perawatan. Dan terkadang hal ini dapat menyebabkan adanya keputihan atau menyebabkan pendarahan yang ringan setelah hubungan seksual. erosi serviks diobati dengan operasi minor. Perawatan ini biasanya dilakukan tanpa adanya rasa sakit. Hindari penggunaan tampon selama beberapa minggu setelah penyakit erosi serviks dinyatakan sembuh.
            </p>
            <p>
              Sangat penting untuk melakukan pemeriksaan kembali ke dokter untuk memastikan bahwa tidak adanya perubahan yang ganas pada serviks (leher rahim) sebelum adanya permukaan yang hancur. Bukan karena erosi serviks yang bersifat ganas namun sangat penting untuk memastikan bahwa melakukan Perawatan harus secara tepat, dengan melakukan pap smear atau kolposkopi dapat diindikasikan jika adanya pendarahan postcoital (tes dimana sekresi serviks diperoleh setelah hubungan seksual dan dianalisa dibawah mikroskop untuk melihat masalah interksi dengan sperma dan lendir serviks).
            </p>

