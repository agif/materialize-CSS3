          <p>
            Untuk menumbuhkan gaya hidup penyebaran penyakit yang tidak sehat dan berkurangnya tingkat kekebalan tubuh, gangguan hormonal, faktor samping dalam Perawatan.
          </p>

          <ul class="browser-default">
            <li>Trauma atau infeksi pada vagina</li>
            <li>Radang serviks (servicitis kronis) atau rahim (endometriosis)</li>
            <li>Risiko yang lebih tinggi dengan darah yang mengalami erosi</li>
            <li>Tekanan keseimbangan hormon yang mendukung hormon estrogen wanita dengan kekurangan progesteron (jika progesteron kurang, stress berkurang dan ditandai dengan perubahan mood yang buruk, dan penambahan berat badan)</li>
            <li>Penyakit ini muncul saat kehamilan</li>
            <li>Aborsi </li>
