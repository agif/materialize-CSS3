          <h6><b>Seberapa Bahayakah Erosi Serviks?</b></h6>

          <ul class="browser-default">
            <li>
              <p class="un-indent">Terlepas dari kenyataan bahwa erosi organ reproduksi mengacu pada proses jinak yang dapat memiliki pengaruh yang paling tidak menguntungkan dan berfungsinya sistem reproduksi dan tubuh perempuan secara umum. Konsekuensi yang paling parah dari parasit ini adalah pembentukan keganasan permukaan mukosa yang rusak. Seringkali tingkat ini meningkat pada wanita 35-45 tahun.</p>
            </li>
            <li>
              <p class="un-indent">Perlunya pemeriksaan preventif rutin oleh seorang ginekolog bukan hanya sebuah keinginan namun kebutuhan vital bagi setiap wanita.</p>
            </li>
            <li>
              <p class="un-indent">Melakukan pemeriksaan minimal 6 bulan sekali.</p>
            </li>
            <li>
              <p class="un-indent">Proses erosif yang mempengaruhi jaringan mukosa organ reproduksi sangat mempermudah penetrasi infeksi secara seksual. akibatnya erosi pada uterus dapat berkembang dengan penyakit kelamin wanita seperti endometriosis, ooforitis, salpingo ooforitis dan servicitis.</p>
            </li>
          </ul>

