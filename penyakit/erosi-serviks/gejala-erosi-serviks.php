          <h3>Gejala Erosi Serviks</h3>

          <br>

          <h6><b>Apa Yang Menjadi Gejala Erosi Serviks</b></h6>
          <h6>Adanya gejala tergantung pada apa yang mendasarinya. Biasanya gejala erosi serviks terjadi pada wanita. Gejala erosi serviks bukan suatu kelainan, namun terkadang gejalanya dikaitkan dengan gejala-gejala seperti berikut:</h6>

          <ol class="browser-default">
            <li>Adanya keputihan yang berlebih dari vagina karena terpapar kelenjar atau lendir yang terletak pada kolumnar epitel</li>
            <li>Adanya pendarahan setelah adanya hubungan seksual karena trauma pada pembuluh darah yang berada pada kolumnar epitel. Biasanya pada wanita hamil yang mengeluhkan adanya bercak pada vagina atau pendarahan. Pendarahan kemungkinan akibat adanya luka pada ektropian</li>
            <li>Pendarahan antar periode atau pendarahan yang tidak normal, pendarahan di sela-sela menstruasi</li>
            <li>Bau busuk dengan warna dari bening hingga kekuningan dan licin</li>
            <li>Dan beberapa dari wanita akan mengalami sakit punggung</li>
            <li>Sakit perut bagian bawah</li>
            <li>Keputihan pada vagina</li>
            <li>Sulit buang air kecil (kencing)</li>
          </ol>

          <p>
            Selama infeksi organ reproduksi, seorang wanita akan mengalami bau yang sangat busuk pada vaginanya dan terkait dengan gejala erosi serviks. Gejala-gejala ini dapat disebabkan oleh kondisi lain juga, jadi sebaiknya segera lakukan konsultasi dengan dokter agar segera diketahui penyebabnya, dan cepat dalam mendapatkan pengobatan agar penyakitnya tidak semakin parah.
          </p>
          <p>
            Erosi serviks mudah ditemukan pada wanita dengan melakukan pemeriksaan ginekologi yang rutin. Namun terkadang ada beberapa pasien dengan erosi serviks dan tidak memiliki gejala sama sekali atau tidak jelas. Biasanya serviks tampak merah dan pemeriksaan pap smear biasanya digunakan untuk menunjukkan tanda-tanda infeksi atau beberapa sel abnormal.
