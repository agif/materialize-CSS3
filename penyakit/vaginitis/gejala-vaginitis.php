          <h6><b>Gejala Penyakit Vaginitis</b></h6>

          <p>
            Gejala penyakit vaginitis bisa sangat jelas atau bisa saja hampir tidak terlihat. Dan terkadang tidak ada gejala sama sekali.Penyakit vaginitis menyebabkan iritasi pada alat kelamin wanita yang dapat menyebabkan rasa terbakar, gatal atau nyeri. Gejala umum lainnya termasuk rasa sakit saat berhubungan seksual atau buang air kecil (BAK atau kencing) dan bau yang tidak sedap. Jika Anda mengalami keputihan yang banyak dan menunjukkan jenis penyakit vaginitis contohnya termasuk:
          </p>
          <br>

          <ul class="browser-default">
            <li class="red-text"><b>Vaginosis bakteri</b>
              <div class="sil-text un-indent">
                Cairan putih ke abu-abuan, berbau busuk, bau nya seperti bau amis biasanya setelah berhubungan seksual
              </div>
            </li>
            <li class="red-text"><b>Infeksi Ragi</b>
              <div class="sil-text un-indent">
                Gejala utamanya adalah adanya rasa gatal dan kemungkinan memiliki keputihan yang berwarna putih, tebal yang menyerupai keju
              </div>
            </li>
            <li class="red-text"><b>Trichomoniasis</b>
              <div class="sil-text un-indent">
                Infeksi yang disebut trichomoniasis dapat menyebabkan keluarnya cairan berwarna kuning kehijauan dan terkadang berbusa
              </div>
            </li>
          </ul>

          <p>
            Infeksi agi menghasilkan cairan putih yang tebal dari alat kelamin wanita yang dapat terlihat seperti keju. Bisa berair dan tidak berbau. Namun infeksi ragi biasanya menyebabkan alat kelamin wanita dan vulva menjadi gatal dan merah. Ketika Anda mengalami gejala trichomoniasis. Gejalanya termasuk gatal, rasa terbakar dan nyeri pada alat kelamin wanita dan vulva. Terasa terbakar saat buang air kecil (BAK atau kencing) serta mengeluarkan cairan berwarna abu-abu atau hijau yang kemungkinan beraroma busuk.
          </p>

