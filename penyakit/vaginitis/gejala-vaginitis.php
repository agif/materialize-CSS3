          <h6><b>Apa Saja Gejala Penyakit Vaginitis?</b></h6>
          <h6>Tanda-tanda atau gejala penyakit vaginitis berbeda-beda tergantung pada jenis kuman yang menginfeksi:</h6>

          <ul class="browser-default">
            <li><u>Bakterial Vaginosis</u>
              <ul>
                <li>Keputihan pada alat kelamin wanita yang kemungkinan tampak putih atau abu-abu dengan bau amis yang merupakan gejala umum yang dapat diamati selama infeksi</li>
                <li>Pada kebanyakan wanita memiliki gejala ringan dan tidak menyadarinya</li>
              </ul>
            </li>
            <li><u>Ragi</u>
              <ul>
                <li><u>Wanita</u>
                  <ul>
                    <li>Keputihan abnormal, rasa gatal, iritasi dan kemerahan pada vulva</li>
                    <li>Terasa terbakar saat buang air kecil (BAK atau kencing) karena urine melewati vulva yang meradang</li>
                    <li>Ketidaknyamanan yang bervariasi</li>
                    <li>Infeksi menular seksual (IMS), klamidia dan gonore (kencing nanah) yang dapat menyebabkan gejala serupa</li>
                  </ul>
                </li>
                <li><u>Pria</u>
                  <ul>
                    <li>Seringnya tidak ada gejala</li>
                    <li>Iritasi yang ringan, rasa gatal atau kemerahan pada alat kelamin pria (balanitis)</li>
                    <li>Infeksi menular seksual (IMS) seperti herpes simpleks virus (HSV), klamidia dan gonore (kencing nanah) yang dapat menyebabkan gejala yang serupa</li>
                  </ul>
                </li>
              </ul>
            </li>
          </ul>

