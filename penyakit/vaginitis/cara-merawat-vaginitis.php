            <h6><b>Perawatan Penyakit Vaginitis</b></h6>

            <p>
              Penyakit vaginitis adalah adanya peradangan pada alat kelamin wanita yang umumnya dapat berkembang seiring dengan peradangan pada vulva. Penyakit vaginitis menular dan dapat disebabkan karena bakteri, jamur atau orgasme parasit. Vaginosis bakterial adalah infeksi bakteri yang paling umum yang dapat menyebabkan vaginitis. Penyakit ini bisa menyebabkan nyeri pada alat kelamin wanita.
            </p>
            <p>
              Penyakit vaginitis adalah kondisi yang umum yang mempengaruhi wanita dari segala usia. Keluarnya cairan dari alat kelamin wanita yang disertai dengan bau yang tidak sedap dan memiliki warna yang berbeda. Kondisi seperti ini menyebabkan iritasi dan nyeri. Orang yang memiliki penyakit vaginitis mengalami ketidaknyamanan yang parah dan iritasi didaerah genital. Hubungan seksual yang menyakitkan adalah tanda lainnya.
            </p>

