          <h6><b>Faktor Risiko Penyakit Vaginitis </b></h6>

          <p>
            Pada wanita lebih rentan untuk terkena penyakit vaginitis jika memiliki diabetes (kencing manis) yang tidak terkontrol, menggunakan produk kebersihan beraroma, melakukan hubungan seks dengan banyak pasangan, berada dibawah tekanan emosional, sedang hamil, pada wanita dengan pil KB dan wanita yang menggunakan douche juga lebih rentan terhadap infeksi pada alat kelamin. semua faktor risiko dapat menjadi ketidakseimbangan hormon atau bakteri di alat kelamin wanita dan vulva yang menyebabkan iritasi dan terkadang infeksi. faktor-faktor yang meningkatkan risiko penyakit vaginitis meliputi:
          </p>

          <ul class="browser-default">
            <li>Perubahan hormonal seperti yang terkait dengan kehamilan, pil KB atau menopause</li>
            <li>Aktifitas seksual</li>
            <li>Memiliki infeksi menular seksual (IMS)</li>
            <li>Obat-obatan lainnya</li>
            <li>Penggunaan alat kontrasepsi untuk pengendalian kelahiran</li>
            <li>Diabetes (kencing manis) yang tidak terkontrol</li>
            <li>Penggunaan produk kebersihan </li>
            <li>Douching</li>
            <li>Mengenakan pakaian basah atau ketat</li>
            <li>Menggunakan alat kontrasepsi (UD) untuk mengontrol kelahiran</li>
          </ul>

