          <h3>Bahaya Vaginitis</h3>

          <br>

          <h6><b>Apa Saja Bahaya Penyakit Vaginitis?</b></h6>

          <p>
            Bahaya penyakit vaginitis pada wanita dengan trikomoniasis atau vaginosis berisiko lebih besar untuk mendapatkan infeksi menular seksual (IMS) karena peradangan yang disebabkan oleh gangguan ini. Pada wanita hamil, vaginosis bakteri simptomatik dan trikomoniasis telah dikaitkan dengan kelahiran prematur dan bayi dengan berat badan rendah.
          </p>
          <p>
            Bahaya atau komplikasi yang sangat serius yang berkaitan dengan penyakit vaginitis sangat jarang sekali terjadi. Namun meninggalkan semua jenus infeksi pada alat kelamin yang tidak ditangani. Peradangan pada alat kelamin membuat wanita secara signifikan untuk menularkan infeksi menular seksual (IMS) terutama HIV.
          </p>

          <ul class="browser-default">
            <li>Vaginosis bakteri dan trikomoniasis dapat meningkatkan risiko penularan HIV dan infeksi menular seksual (IMS) lainnya</li>
            <li>Selama kehamilan, vaginosis bakteri dan trikomoniasis meningkatkan kelahiran prematur</li>
            <li>Pasien dengan HIV dapat menularkan infeksi ragi yang parah dan persisten yang tidak responsif terhadap perawatan</li>
          </ul>

