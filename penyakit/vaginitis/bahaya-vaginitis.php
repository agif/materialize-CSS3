          <h6><b>Apa Komplikasi Dari Bakterial Vaginosis?</b></h6>

          <p>
            Ketika penyakit vaginitis tidak ditangani maka dapat menyebabkan komplikasi serius dan risiko kesehatan termasuk:
          </p>

          <ul class="browser-default">
            <li>Komplikasi kehamilan, pada wanita hamil kemungkinan melahirkan secara dini atau bayi nya dengan berat badan rendah. Dan memiliki peluang lebih besar untuk menularkan jenis infeksi lain setelah melahirkan</li>
            <li>Infeksi menular seksual (IMS), meningkatkan risiko terkena infeksi menular seksual (IMS) termasuk virus herpes simpleks, klamidia dan HIV</li>
            <li>Penyakit Vaginitis (PID) dapat menyebabkan penyakit Vaginitis (PID), infeksi pada organ reproduksi pada wanita dan kondisi ini dapat meningkatkan risiko infertilitas atau kemandulan</li>
            <li>Infeksi setelah pembedahan atau operasi, berisiko lebih tinggi untuk infeksi setelah operasi yang mempengaruhi sistem reproduksi. Termasuk histerektomi, aborsi dan kelahiran caesar</li>
          </ul>

