            <h6><b>Diagnosis Vulvitis</b></h6>

            <p>
              Untuk mendiagnosa vulvitis dokter biasanya akan memeriksa panggul pasien dan melihat  adanya tanda-tanda yang mengindikasikan vulvitis seperti bengkak,merah,  benjolan berisi cairan atau luka pada vulva. Selain itu dokter juga akan mengecek adanya keputihan sebagai tanda-tanda dari infeksi.
            </p>
            <p>
              Selain menanyakan riwayat kesehatan dan pemeriksaan fisik biasanya dokter dapat melakukan tes darah, tes urine, dan pap smear guna untuk mendeteksi penyebab peradangan atau infeksi yang terjadi. Selain itu terkadang juga diperlukan mengambil sampel jaringan kulit untuk pemeriksaan di bawah mikroskop (biopsi kulit), apabila vulvitis tidak membaik dengan penanganan yang sudah dilakukan.
            </p>

