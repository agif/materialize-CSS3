          <h3>Gejala Vulvitis</h3>

          <br>

          <p>
            Vulvitis merupakan suatu infeksi atau peradangan yang terjadi pada bagian organ intim luar wanita yang disebut dengan vulva.Vulva adalah lipatan bagian kulit yang terletak pada bagian luar organ intim wanita, vulva sering disalah artikan orang awam sebagai alat kelamin wanita. Padahal vulva dan alat kelamin wanita berbeda. Alat kelamin wanita merupakan liang atau saluran yang terletak lebih dalam setelah melewati vulva. Vulva terdiri dari dua yaitu labia mayora dan labia minora serta klitoris.
          </p>
          <p>
            Kulit vulva sangat rentan mengalami iritasi disebabkan karena lembab, serta iritasi dapat disebabkan oleh reaksi alergi inifeksi ataupun luka. Wanita berbagai usia dapat mengalami vulvitis. Setiap wanita yang mempunyai alergi tertentu, infeksi atau penyakit yang dapat memancing tumbuhnya peradangan vulva dapat beresiko terkena penyakit ini. Wanita yang belum mencapai pubertas atau wanita yang mengalami menopause juga sangat beresiko terkena infeksi vulva, karena tingkat esterogen mereka yang rendah sehingga dapat terjangkit penyakit ini.
          </p>

