          <h6><b>Bagaimana Gejala Dari Vulvitis?</b></h6>

          <p>
            Vulvitis dapat menimbulkan gejala bervariasi, tergantung dari penyebab peradangan yang terjadi pada vulva. Sangat disarankan untuk tidak menggaruk alat kelamin jika muncul rasa gatal, karena vulvitis berisiko menyebabkan iritasi berkembang menjadi infeksi.
          </p>

          <h6>Gejala-gejala vulvitis di antaranya adalah:</h6>

          <ul class="browser-default">
            <li>Bengkak dan merah di labia dan vulva.</li>
            <li>Benjolan berisi cairan (blister) pada vulva.</li>
            <li>Perlu diingat bahwa gejala-gejala di atas bisa saja disebabkan oleh penyakit lain, selain vulvitis. Untuk itu, konsultasikan kepada dokter bila Anda mengalami sejumlah gejala di atas.</li>
            <li>Rasa sangat gatal di alat kelamin, terutama pada malam hari.</li>
            <li>Keputihan</li>
            <li>Rasa seperti terbakar dan kulit pecah-pecah di sekitar vulva.</li>
            <li>Kulit bersisik dan area putih yang menebal di vulva.</li>
          </ul>

