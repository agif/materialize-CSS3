          <h6><b>Penyebab Penyakit Cystitis Lainnya Oleh Kerusakan Atau Iritasi Di Daerah Uretra</b></h6>

          <p>
            Cystitis juga bisa disebabkan oleh kerusakan atau iritasi didaerah sekitar uretra baik pada pria maupun pada wanita dan bisa jadi hasil dari:
          </p>

          <h6>Penyakit Cystitis Juga Bisa Menyerang Anak-Anak, Penyebabnya Termasuk:</h6>

          <ul class="browser-default">
            <li>Kerusakan yang disebabkan ketika mengganti kateter (tabung yang dimasukkan ke uretra untuk urine mengalir ke kantong drainase yang sering digunakan setelah operasi)</li>
            <li>Kerusakan atau adanya memar yang disebabkan oleh seks yang kuat atau sering</li>
            <li>Pakaian yang ketat</li>
            <li>Iritasi kimia </li>
            <li>Masalah kandung kemih atau ginjal lainnya seperti infeksi ginjal atau radang prostat</li>
            <li>Diabetes (kencing manis) kondisi yang disebabkan karena terlalu banyak glukosa dalam darah</li>
          </ul>

          <p>
            Cystitis biasanya disebabkan ketika bakteri dari luar tubuh memasuki uretra dan berkembang biak didalam kandung kemih dan saluran kemih. Cystitis bakterial paling sering terjadi pada wanita dan sangat jarang sekali terjadi pada pria. Diantara wanita yang aktif secara seksual, hubungan seksual dapat menyebabkan bakteri ke kandung kemih dan menyebabkan infeksi.
          </p>
          <p>
            Pada wanita dewasa dapat mengalami cystitis yang disebabkan oleh bakteri yang hidup didalam dan disekitar alat kelamin wanita. Pada setiap wanita yang menggunakan diafragma, sedang hamil atau telah mengalami menopause akan berisiko tinggi untuk cystitis. Cystitis tidak disebabkan oleh infeksi bakteri.
          </p>

