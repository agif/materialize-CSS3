          <h6><b>Cystitis Pada Wanita</b></h6>

          <p>
            Pada wanita pembukaan uretra (tabung yang melewati kandung kemih keluar dari tubuh) sangat dekat dengan pembukaan anus (dubur atau anal). Ada bakteri disekitar anus yang dapat ditransfer ke uretra. Bakteri yang dapat masuk ke kandung kemih dan dapat menyebabkan adanya iritasi. Pada wanita, cystitis sering disebabkan oleh bakteri ketika:
          </p>

          <ul class="browser-default">
            <li>Memasukkan tampon</li>
            <li>Berhubungan seks</li>
            <li>Menyeka kembali alat genital dari belakang ke depan (berulang kali) dan bukan dari depan ke belakang</li>
            <li>Penggunaan diafragma untuk kontrasepsi</li>
