          <h6><b>Apa Sajakah Yang Dapat Menyebabkan Penyakit Cystitis?</b></h6>

          <h6>
            Penyebab penyakit cystitis adalah adanya infeksi bakteri. Jika bakteri mencapai kandung kemih maka dapat berkembang biak dan mengiritasi lapisan kandung kemih yang dapat menyebabkan gejala cystitis. Infeksi bakteri dapat disebabkan karena tidak mengosongkan kandung kemih sepenuhnya karena alasan:
          </h6>

          <ul class="browser-default">
            <li>Hamil karena ada tekanan didaerah panggul (pelvic)</li>
            <li>Memiliki penyumbatan disuatu tempat di sistem kemih dan bisa disebabkan oleh tumor pada pria dan pembesaran prostat (kelenjar yang terletak diantara alat kelamin pria dan kandung kemih)</li>
          </ul>

