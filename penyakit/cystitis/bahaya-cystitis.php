          <h6><b>Faktor-Faktor Terkait Dengan Risiko Lebih Tinggi Penyakit Cystitis:</b></h6>

          <br>

          <ul>
            <li>Hubungan seksual, pada wanita yang didiagnosis penyakit cystitis lebih sering daripada pria. Gejala pada pria dapat menyerupai cystitis dan lebih sering dikaitkan dengan peradangan kelenjar prostat (prostatitis)</li>
            <li>Warna kulit dan rambut, memiliki kulit dan rambut merah dikaitkan dengan risiko lebih besar dari penyakit cystitis</li>
            <li>Usia, pada kebanyakan orang dengan penyakit cystitis akan didiagnosis pada usia 30 tahun lebih</li>
            <li>Mengalami gangguan nyeri yang sangat kronis, penyakit cystitis dapat berhubungan dengan gangguan nyeri yang kronis lainnya seperti sindrom iritasi pada usus</li>
          </ul>

          <p>
            Salah satu pencegahan adalah dengan cara minum air mineral yang cukup untuk menghindari segala gejala penyakit cystitis yang akan ditimbulkan. Penyakit cystitis pada umumnya tidak berbahaya namun infeksinya dapat menyebar terutama ke daerah atau area ginjal yang dapat menyebabkan penyakit serius seperti penyakit peradangan pada ginjal. Oleh karena itu untuk mencegah terjadinya komplikasi tersebut, segera lakukan konsultasi dan pemeriksaan lebih lanjut dengan dokter.
