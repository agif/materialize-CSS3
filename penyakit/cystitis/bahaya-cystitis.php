          <h3>Bahaya Penyakit Cystitis</h3>

          <br>

          <h6><b>Apa Yang Menjadi Bahaya Pada Penyakit Cystitis?</b></h6>

          <p>
            Bahaya penyakit cystitis dapat mengakibatkan sejumlah komplikasi termasuk:
          </p>

          <ul class="browser-default">
            <li>Mengurangi kapasitas kandung kemih, penyakit cystitis dapat menyebabkan dinding kandung kemih menjadi kaku yang memungkinkan kandung kemih menahan lebih sedikit urine</li>
            <li>Kualitas hidup yang lebih rendah, seringnya buang air kecil (BAK atau kencing) dan rasa sakit dapat mengganggu aktifitas sosial, pekerjaan dan kegiatan lain dalam kehidupan sehari-hari</li>
            <li>Masalah keintiman seksual, seringnya buang air ekcil (BAK atau kencing) dan rasa sakit akibat ketegangan hubungan dan keintiman seksual penderitanya</li>
            <li>Masalah emosional, rasa sakit yang kronis dan tidur yang terganggu terkait dengan penyakit cystitis dapat menyebabkan stress emosional dan dapat menyebabkan depresi</li>
          </ul>

