          <h6><b>Faktor Risiko Lainnya Mengenai Penyakit Cystitis</b></h6>

          <p>
            Penyakit cystitis yang terkomplikasi biasanya akan mengalami infeksi berulang. Penyakit cystitis dinyatakan kronis dan berulang jika pasiennya menderita gejala penyakit cystitis setahun beberapa kali. Namun yang menjadi faktor risiko penyakit cystitis bisa meliputi:
          </p>

          <ul class="browser-default">
            <li>Batu kandung kemih</li>
            <li>Gangguan saat mengosongkan kandung kemih sebagai akibat dari penyakit prostat di kandung kemih</li>
            <li>Hubungan seksual</li>
            <li>Kehamilan</li>
            <li>Adanya luka jaringan dan tonjolan di kandung kemih</li>
            <li>Kateter</li>
            <li>Sistem kekebalan tubuh yang melemah karena AIDS</li>
            <li>Hipotermia</li>
            <li>Penyakit seperti diabetes (kencing manis)</li>
            <li>Tumor</li>
          </ul>

