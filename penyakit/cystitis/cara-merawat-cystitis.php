            Beberapa perawatan kemungkinan akan bekerja lebih baik. Tujuan perawatan adalah untuk membantu setiap orang menyembuhkan gejalanya. Merekomendasikan perawatan kepada seseorang akan sulit karena harus dilakukan tes atau pengujian di laboratorium terlebih dahulu untuk mengetahui penyebab pastinya. Yang dianggap berpotensi menyebabkan gejalanya dapat terjadi.
