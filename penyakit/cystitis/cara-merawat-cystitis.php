              <h6><b>Pencegahan Untuk Penyakit Cystitis</b></h6>

              <h6>Penyakit cystitis tidak dapat dicegah akan tetapi dengan tindakan berikut dapat membantu:</h6>

              <ul class="browser-default">
                <li>Kebersihan yang baik setelah berhubungan seksual</li>
                <li>Gunakan produk untuk alat kelamin yang tidak mengandung bahan kimia</li>
                <li>Selalu kosongkan kandung kemih saat buang air kecil (BAK atau kencing)</li>
                <li>Tidak menunda rasa ingin buang air kecil (BAK atau kencing)</li>
                <li>Menyeka dari depan ke belakang</li>
                <li>Gunakan pelumas saat berhubungan seksual</li>
              </ul>

