          <h6><b>Berapa Biaya Perawatan Penyakit Cystitis?</b></h6>

      		<p>
            Biaya perawatan penyakit cystitis yang ada di Jakarta Indonesia, penyakit cystitis adalah peradangan pada lapisan dalam kandung kemih. Penyakit cystitis adalah kondisi yang biasanya menyerang wanita, meskipun pada pria dan anak-anak juga bisa terkena. Penyakit cystitis lebih sering terjadi pada wanita ketika hamil dan setelah menopause.
          </p>
          <p>
            Penyebab yang paling sering pada penyakit cystitis adalah ketika bakteri yang biasanya hidup di anus (anal), dan masuk ke uretra dan berjalan ke kandung kemih. Hal seperti ini dapat terjadi selama hubungan seksual atau saat masuknya tampon dengan menyeka atau mencuci bagian alat kelamin dari area belakang ke area depan. Dan pada wanita yang menggunakan alat kontrasepsi juga kemungkinan akan berisiko terkena penyakit cystitis. Jika kandung kemih tidak dikosongkan maka dapat menyebabkan bakteri berkembang biak, dan kondisi seperti ini sangat umum terjadi pada wanita hamil karena adanya tekanan pada daerah panggul (pelvic).
          </p>
          <p>
            Seperti yang diketahui biaya perawatan penyakit tidaklah murah. Biaya perawatan suatu penyakit tergantung dari bagaimana cara perawatannya, dimana tempat perawatannya, serta seberapa parah tingkat keparahannya dan lain-lainnya. Oleh karena itu, jika Anda mengalami adanya gejala maka harus segera melakukan konsultasi dengan dokter dan pemeriksaan untuk segera ditindak lanjuti agar penyakitnya tidak semakin parah.
