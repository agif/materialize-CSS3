          <h6><b>Perawatan Penyakit Cystitis</b></h6>

          <p>
            Gejala biasanya bisa membaik dalam beberapa hari setelah perawatan. Tidak menggunakan perawatan apapun yang dapat menjadi pilihan jika gejalanya ringan (jika Anda tidak sedang hamil dan jika Anda tidak memiliki penyakit lain). Sistem kekebalan tubuh dapat membersihkan infeksi. Perawatan dapat mengurangi rasa sakit atau ketidaknyamanan dan juga dapat menurunkan suhu tinggi (demam). Jika gejalanya memburuk atau Anda mengalami demam harus segera konsultasikan atau melakukan pemeriksaan dengan dokter.
          </p>

