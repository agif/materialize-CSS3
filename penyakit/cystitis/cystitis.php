      		<h3>Cystitis</h3>

          <br>

          <h6><b>Apa Itu Cystitis?</b></h6>

      		<p>
            Cystitis adalah peradangan pada kandung kemih. Cystitis (infeksi kencing) atau infeksi urine di kandung kemih sering terjadi pada wanita. Cystitis berarti radang pada kandung kemih. Dan biasanya disebabkan oleh infeksi urine. Anda kemungkinan juga megalami rasa nyeri di perut bagian bawah, darah dalam urine dan suhu badan yang meningkat (demam).
          </p>
          <p>
            Air seni juga bisa menjadi keruh dan kemungkinan menjadi bau. Beberapa bakteri berada dibelakang anus setelah buang air besar (BAB atau feses).  Bakteri ini terkadang melewati tabung dari kandung kemih yang keluar (urine) dan masuk ke kandung kemih. Beberapa bakteri berkembang dalam air kencing dan berkembang biak dengan cepat untuk menyebabkan infeksi.
          </p>

          <br>

          <ul class="browser-default red-text">
            <li>Cystitis Pada Wanita
              <p class="un-indent sil-text">
                Cystitis adalah masalah pada wanita yang paling umum dan diperkirakan wanita dewasa mengalami disuria (buang air kecil yang menyakitkan) atau bisa saja memiliki infeksi saluran kemih (ISK) bakteri.
              </p>
            </li>
            <br>
            <li>Cystitis Pada Pria
              <p class="un-indent sil-text">
                Infeksi saluran kencing (ISK) jarang sekali terjadi pada pria usia muda dan setengah baya namun dapat terjadi sebagai komplikasi infeksi bakteri ginjal atau kelenjar prostat.
              </p>
            </li>
            <br>
            <li>Cystitis Pada Anak-Anak
              <p class="un-indent sil-text">
                Pada anak-anak cystitis disebabkan oleh kelainan kongenital (ada saat lahir) dari saluran kemih. Vesicoureteral reflux adalah suatu kondisi dimana anak tidak dapat sepenuhnya mengosongkan kandung kemih. Kondisi seperti ini kemungkinan urine tetap masuk atau mengalir ke belakang (refluks) ke dalam kandung kemih yang sebagian kosong. Selain itu, cystitis juga dapat disebabkan oleh penyekaan ke dapan dan bukan ke belakang setelah buang air besar (BAB) terutama pada anak perempuan.
              </p>
            </li>
          </ul>
