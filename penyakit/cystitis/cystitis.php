          <h6><b>Apa Sajakah Penyebab Penyakit Cystitis </b></h6>

          <p>
            Penyebab penyakit cystitis sangat bervariasi bisa menurut jenis kelamin karena perbedaan dalam struktur anatomi saluran kemih. Dan sebagian besar infeksi urine disebabkan oleh kuman (bakteri) yang berasal dari usus. Pada perempuan akan jauh lebih mungkin terkena daripada laki-laki untuk memiliki penyakit cystitis, karena tabung yang mengeluarkan urine dari kandung kemih wanita (uretra) lebih pendek dan membuka lebih dekat bagian belakang (anus atau dubur). Penyebab penyakit cystitis lainnya termasuk:
          </p>

          <ul class="browser-default">
            <li>Memiliki diabetes (kencing manis) mellitus</li>
            <li>Sedang hamil</li>
            <li>Aktif secara seksual</li>
            <li>Penggunaan alat kontrasepsi</li>
            <li>Setelah mengalami menopause, perubahan pada jaringan alat kelamin wanita dan uretra setelah menopause membuat mereka lebih sulit untuk bertahan dalam menahan infeksi</li>
            <li>Memiliki kateter dikandung kemih</li>
            <li>Memiliki kelainan pada ginjal, kandung kemih atau sistem saluran kencing</li>
            <li>Memiliki sistem kekebalan yang tidak berfungsi dengan baik (misalnya karena AIDS atau yang menekan sistem kekebalan tubuh)</li>
          </ul>

