          <h6><b>Apa Saja Gejala Pada Penyakit Cystitis?</b></h6>

          <p>
            Gejala penyakit cystitis yang khas adalah adanya rasa sakit ketika buang air kecil (BAK atau kencing) yang sangat sering (frekuensi kencing). Cystitis dapat menyebabkan berbagai gejala utama, meliputi:
          </p>

          <ul class="browser-default">
            <li>Buang air kecil (BAK atau kencing) yang lebih sering (urgensi kencing)</li>
            <li>Rasa terbakar atau seperti menyengat saat buang air kecil (BAK atau kencing)</li>
            <li>Adanya darah dalam urine (hematuria)</li>
            <li>Ketidaknyamanan di perut bagian bawah</li>
            <li>Merasa lelah dengan suhu yang terus meningkat (demam)</li>
          </ul>

