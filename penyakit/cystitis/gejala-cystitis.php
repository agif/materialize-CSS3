          <h3>Gejala Penyakit Cystitis</h3>

          <br>

          <h6><b>Apa Sajakah Gejala Pada Penyakit Cystitis?</b></h6>

          <h6>
            Gejala penyakit cystitis yang dialami kebanyakan orang seperti:
          </h6>

          <ul class="browser-default">
            <li>Seperti rasa terbakar atau menyengat saat buang air kecil (BAK atau kencing) </li>
            <li>Selalu ingin buang air kecil (urgensi kencing) tetapi hanya sedikit jumlah air kencing yang keluar</li>
            <li>Tidak bisa sepenuhnya mengosongkan kandung kemih</li>
          </ul>

          <h6>
            Namun, gejala penyakit cystitis ini tidak biasa dan dapat disebabkan oleh kondisi lain seperti:
          </h6>

          <ul class="browser-default">
            <li>Urine yang berbau sangat kuat</li>
            <li>Air kencing yang berwarna gelap </li>
            <li>Adanya darah dalam air kencing (hematuria)</li>
            <li>Nyeri pada bagian atau area perut dan sakit pada punggung</li>
            <li>Mengalami demam dengan suhu yang sangat tinggi dan merasa sangat lemah</li>
          </ul>

