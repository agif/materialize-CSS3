          <h6><b>Gejala Penyakit Cystitis Yang Paling Umum Pada Wanita Dan Pria</b></h6>

          <ul class="browser-default">
            <li>Nyeri saat buang air kecil (BAK atau kencing)</li>
            <li>Seringnya buang air kecil (urgensi kencing) dari biasanya namun jumlah urine yang keluar hanya sedikit</li>
            <li>Air kencing yang berwarna tidak biasa seperti berwarana coklat, kekuningan dan gelap atau keruh dan berbau busuk atau tidak enak</li>
            <li>Adanya bercak darah di urine</li>
            <li>Terjadi sakit pada daerah perut atau punggung</li>
            <li>Mengalami demam dan lemah</li>
            <li>Rasa sakit dan merasakan seperti sensasi terbakar atau menyengat saat buang air kecil (BAK atau kencing)</li>
            <li>Adanya darah dalam urine (hematuria)</li>
            <li>Rasa sakit dibagian bawah perut</li>
            <li>Nyeri diatas tulang kemaluan, bagian punggung bawah dan perut</li>
          </ul>

          <p>
            Dalam kasus seperti ini, gejala penyakit cystitis sering kambuh atau ketika Anda menderita gejala yang lebih parah kemungkinan harus segera melakukan perawatan lebih lanjut mengenai penyakit cystitis. Penyakit cystitis yang terjadi ketika bakteri yang biasanya hidup di usus dan masuk ke saluran kemih.
          </p>
          <p>
            Kondisi seperti ini bisa terjadi ketika Anda menyeka alat kelamin dari belakang ke depan, bukan dari depan ke belakang. Kandung kemih adalah bagian tubuh yang biasanya harus terindar dari adanya bakteri. Ketika adanya bakteri yang memasuki kandung kemih maka dapat menyebabkan infeksi dan mengarah ke gejala penyakit cystitis.
          </p>

          <h6>Penyakit Cystitis Juga Bisa Menyerang Anak-Anak, Gejalanya Termasuk:</h6>

          <ul class="browser-default">
            <li>Selalu merasa lemah</li>
            <li>Mempunyai sifat yang lekas marah</li>
            <li>Berkurangnya nafsu makan</li>
            <li>Muntah</li>
            <li>Adanya rasa nyeri saat buang air kecil (BAK atau kencing)</li>
          </ul>

          <p>
            Oleh karena itu, segera konsultasikan dengan dokter dan lakukan pemeriksaan untuk segera ditindak lanjuti. Karena gejala penyakit cystitis sangat membingungkan untuk didiagnosisnya ketika belum dilakukan diagnosis dengan melakukan pengecekan di laboratorium, dengan gejala-gejala seperti:
          </p>

          <ul class="browser-default">
            <li>Infeksi menular seksual (IMS)</li>
            <li>Infeksi menular seksual (IMS) seperti gonore (kencing nanah) atau klamidia</li>
            <li>Terinfeksi bakteri escherichia coli (e.coli)</li>
            <li>Sariawan pada alat kelamin wanita atau dikenal dengan candida (infeksi jamur)</li>
            <li>Radang pada uretra (urethritis)</li>
            <li>Dindrom uretra (pada wanita)</li>
            <li>Radang kelenjar prostat atau prostataitis (pada pria)</li>
          </ul>

