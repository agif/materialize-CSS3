          <h3>Perawatan Keputihan</h3>

          <br>

          <p>
            Jika Anda mengalami gejala keputihan yang semakin mengganggu, sebaiknya segera untuk diatasi sesuai dengan penyebabnya. Keputihan yang tidak normal (patologis) memang perlu untuk mendapatkan penanganan segera. Biasanya memiliki tanda gejala seperti jumlah yang keluar dari vagina menjadi tambah banyak, berwarna kuning, hijau, abu-abu dan bisa sampai berbusa, terkadang juga keputihan yang terjadi disertai dengan darah, terasa gatal, panas, dan menyengat. Tetapi bagaimana cara mengatasinya?
          </p>
          <p>
            Pasien yang menderita penyakit ini pernah berpikir untuk mencoba membeli beberapa obat-obatan dan berharap dapat lebih cepat mengatasi penyakitnya. Tetapi sebaiknya sebelum diketahui penyebab dari penyakit tersebut jangan sembarangan menggunakan obat tanpa resep dokter.salah penanganan dapat mengakibatkan penyakit bertambah parah. Cara yang paling baik untuk terlepas dari penyakit yang bermasalah ini dapat dilakukan dengan mencari tahu apa penyebabnya.
          </p>
          <p>
            Seringkali keputihan sulit diatasi karena penyebab keputihan bermacam-macam dan bisa terinfeksi beberapa kali sekaligus yang pada umumnya adalah jamur, bakteri atau parasit. Oleh sebab itu penanganan yang sesuai dengan penyebabnya akan menjadi salah satu cara terbaik untuk mengatasi masalah ini. Keputihan yang disebabkan oleh reaksi alergi atau iritasi dapat dilakukan dengan menghindari penyebabnya. Cobalah untuk mengganti deterjen, sabun, pembalut atau benda lain yang memicunya.
          </p>

