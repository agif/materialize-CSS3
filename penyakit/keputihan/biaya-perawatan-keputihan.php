      		<h3>Biaya Perawatan Keputihan</h3>

          <br>

      		<p>
            Bila Anda memiliki keluhan keputihan dan ingin berkonsultasi dengan dokter untuk mengatasi keluhan Anda saat ini, tetapi bingung dengan biaya yang akan dikeluarkan karena terhambat karena faktor ekonomi dan menganggap biaya perawatan untuk keputihan mahal, sehingga Anda menunda nunda untuk datang berobat pada dokter. Tetapi tahukah Anda jika penundaan perawatan pada dokter dapat semakin memperparah kondisi Anda, sehingga biaya yang dikeluarkan akan semakin banyak karena keputihan yang abnormal harus segera diatasi.
          </p>

