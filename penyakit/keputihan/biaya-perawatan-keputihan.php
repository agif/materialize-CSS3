	        <h6><b>Berapa Biaya Perawatan Keputihan? </b></h6>

          <p>
          	Keputihan disebabkan oleh berbagai macam penyebab, sehingga biaya perawatan yang dikeluarkan oleh setiap pasien berbeda-beda tergantung dari penyebab dan tingkat keparahan yang dialami oleh pasien.Untuk mengetahui jenis infeksi pada keputihan yang Anda alami biasanya dokter akan melakukan pemeriksaan fisik dan medis seperti analisa swab alat kelamin, tes lendir keputihan, tes spekulum dan lain lain akan diperlukan. Dengan mengetahui penyebabnya maka dokter bisa memberikan penanganan terhadap kondisi keputihan Anda dengan sesuai. Biaya yang dikeluarkan setiap pasien berbeda-beda sesuai dengan pemeriksaan seperti apa yang dilakukan, serta penatalaksaan seperti apa yang dilakukan.
          </p>

          <br>
          <img class="responsive-img materialboxed" src="penyakit/keputihan/thumbnail/Biaya-Perawatan-Keputihan.jpg">
          <br>

          <p>
            Jika Anda mengalami keputihan sebaiknya segera konsultasikan diri Anda pada dokter, dengan begitu Anda dapat mengetahui biaya perawatan serta penanganan sedini mungkin dapat mencegah berbagai komplikasi akibat keputihan. Jika dibiarkan berlarut-larut keputihan ini dapat semakin menyebar pada organ bagian dalam seperti menginfeksi pada organ reproduksi lainnya seperti rahim, tuba fallopi, indung telur hingga hal ini dapat menyebabkan infertilitas (kemandulan).
          </p>

