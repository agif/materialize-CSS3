	        <h6><b>Dimana Saya Bisa Mendapatkan Tempat Terbaik Perawatan Keputihan?</b></h6>

          <p>
           	Jika Anda mengalami keputihan, sebaiknya jangan mencoba penanganan sendiri tanpa berkonsultasi dengan dokter karena yang di khawatirkan jika salah penanganan dapat semakin memperparah kondisi yang Anda alami saat ini. Konsultasikan diri Anda segera agar mendapatkan penanganan sedini mungkin dan biaya penanganan pun akan lebih mudah dilakukan.
          </p>

