          <h6><b>Kenali Gejala Keputihan Abnormal, Antara Lain Adalah</b></h6>

          <h6><b>Gejala Keputihan Meliputi:</b></h6>

          <ol class="browser-default">
            <li><b>Keputihan yang disertai dengan gatal</b>
              <p class="un-indent">
                Jika Anda mengalami keputihan yang disertai dengan gatal sebaiknya Anda harus waspada, karena keputihan yang tidak memiliki bau, dengan tekstur yang lebih cair yang menyebabkan rasa gatal bisa disebabkan oleh infeksi clamidia. Keputihan ini termasuk jenis infeksi yang sangat umum terjadi namun tetap membutuhkan perawatan hingga bisa sembuh. Keputihan ini juga bisa disebabkan oleh penyakit menular seksual. Jika mengalami hal ini sebaiknya segera periksakan diri Anda pada dokter karena hal yang paling berbahaya adalah jika infeksi menyebar hingga ke saluran kemih yang bisa menyebabkan infeksi saluran kemih.
              </p>
            </li>
            <br>
            <li><b>Bau keputihan berbau busuk</b>
              <p class="un-indent">
                Wanita yang mengalami keputihan dengan bau yang busuk sebaiknya harus waspada karena hal ini bisa menjadi tanda bahwa infeksi sudah parah yang biasa ditandai dengan bentuk yang lebih kental, aroma yang menyengat dan juga warna yang hijau atau kekuningan. Terkadang keputihan yang berbau busuk ini juga dapat menjadi pertanda infeksi atau penyakit yang lebih berat seperti misalnya kanker.
              </p>
            </li>
            <br>
            <li><b>Keputihan disertai dengan pendarahan</b>
              <p class="un-indent">
                Keputihan ini bisa sangat berbahaya karena menandakan ada masalah pada organ reproduksi. Keputihan yang disertai dengan pendarahan bisa terjadi pada masa setelah menstruasi atau menjelang menopause. Tetapi beberapa wanita yang mengalami keputihan ini ternyata mendapatkan diagnosis kanker rahim atau kanker serviks.
              </p>
            </li>
            <br>
            <li><b>Keputihan cair tapi terus menerus</b>
              <p class="un-indent">
                Keputihan yang cair seperti keputihan biasa namun keluar secara terus menerus. Hal ini harus Anda waspadai karena bisa saja suatu kondisi serius yang hatrus segera untuk diatasi.
              </p>
            </li>
            <br>
            <li><b>Keputihan disertai demam</b>
              <p class="un-indent">
                Beberapa wanita juga biasanya mengalami keputihan yang disertai dengan demam sehingga Anda harus waspada. Demam yang terjadi dapat menjadi suatu petunjuk bahwa keputihan menjadi infeksi yang sudah mempengaruhi sistem tubuh. Untuk memastikan penyebab dokter akan melakukan pengujian di laboratorium sangat diperlukan untuk mengetahui apa penyebab keputihan yang sebenarnya.
              </p>
            </li>
            <br>
            <li><b>Keputihan disertai dengan nyeri panggul</b>
              <p class="un-indent">
                Wanita yang mengalami keputihan yang disertai dengan nyeri panggul juga harus sangat waspada. Keputihan ini bisa terjadi akibat infeksi serius. Nyeri panggul = dapat terjadi akibat adanya peradangan pada bagian organ dalam panggul. Untuk memastikan penyebab yang sebenarnya maka pemeriksaan oleh dokter sangat diperlukan.
              </p>
            </li>
            <br>
            <li><b>Keputihan dengan tekstur kental</b>
              <p class="un-indent">
                Keputihan dengan tekstur yang lebih kental biasanya terjadi pada wanita menjelang menstruasi. Biasanya satu minggu sebelum menstruasi memang keputihan ini terjadi akibat perubahan hormon sesaat. Namun jika keputihan berlangsung lebih lama maka bisa menjadi pertanda ada penyakit tertentu sehingga memberi dampak yang serius. Infeksi dari trichomoniasis juga bisa terjadi dengan gejala keputihan seperti ini.
              </p>
            </li>
            <br>
            <li><b>Keputihan lebih kental dan sangat putih</b>
              <p class="un-indent">
                Keputihan dengan tanda putih dan lebih kental bisa terjadi akibat infeksi jamur. Wanita yang mengalami keputihan ini sebaiknya juga tidak menggunakan sabun atau produk apapun untuk membersihkan organ intim, menggunakan pakaian yang basah, dan sebaiknya tidak berenang untuk sementara waktu hingga keputihan sembuh.
              </p>
            </li>
            <br>
            <li><b>Keputihan hijau dan berbusa</b>
              <p class="un-indent">
                Keputihan yang berwarna hijau, berbusa dan aroma yang tidak enak bisa disebabkan oleh  gonorrhea. Ini salah satu penyakit menular seksual yang harus mendapatkan perawatan medis secara intensif. Keputihan yang disebabkan oleh jamur terkadang juga bisa menunjukkan tanda ini. Namun untuk mendapatkan hasil yang lebih pasti biasanya dokter akan melakukan pengujian.
              </p>
            </li>
            <br>
            <li><b>Keputihan dengan bau amis</b>
              <p class="un-indent">
                Keputihan yang disertai dengan bau amis bisa menjadi ciri cici keputihan yang berbahaya. Kondisi ini juga biasanya disertai dengan nyeri pinggang, sakit saat buang air kecil dan tidak nyaman pada organ intim. Keputihan ini bisa terjadi akibat keseimbangan mikroorganisme yang tidak seimbang dalam organ intim. Beberapa obat seperti antibiotik bisa diperlukan dengan resep dokter. Wanita dengan keputihan ini juga sebaiknya tidak melakukan hubungan seksual bebas atau  bersama pasangan tetap.
              </p>
            </li>
          </ol>

