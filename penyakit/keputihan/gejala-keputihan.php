          <h3>Gejala Keputihan</h3>

          <br>

          <p>
            Banyak wanita yang tidak memiliki kesadaran terhadap risiko bahaya keputihan yang tidak normal, bahkan ada yang menganggap keputihan itu suatu yang lazim, tentu benar keputihan merupakan suatu yang lazim tetapi kita harus bisa membedakan mana keputihan normal dan mana keputihan abnormal.Keputihan normal pada umumnya memiliki ciri lendir berwarna putih jernih atau keputihan, tidak menyebabkan gatal, tidak menyebabkan nyeri, dan tidak memiliki keluhan lainnya.
