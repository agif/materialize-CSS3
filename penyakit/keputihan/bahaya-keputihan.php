          <h6><b>Apa Saja Bahaya Keputihan Jika Tidak Segera Diatasi?</b></h6>

          <ol class="browser-default">
            <li>
              <p class="un-indent">
                Keputihan yang tidak segera diatas dapat menimbulkan berbagai komplikasi, serta keputihan abnormal yang terjadi juuga dapat menjadi salah satu gejala dari penyakit seperti infeksi menular seksual sehingga dapat menyebabkan kondisi serius jika tidak mendapatkan penanganan segera.
              </p>
            </li>
            <li>
              <p class="un-indent">
                Keputihan abnormal yang terjadi dapat pula berbahaya, karena keputihan yang terjadi dapat menjadi salah satu tanda terjadinya suatu masalah di dalam tubuh wanita tersebut pada wanita.
              </p>
            </li>
            <li>
              <p class="un-indent">
                Keputihan tidak normal kemungkinan dapat mengakibatkan infertilitas atau kemandulan, hal ini terjadi karena infeksi yang terjadi pada bagian alat kelamin yang tidak mendapatkan penanganan segera,maka dapat menyebar hingga pada system reproduksi bagian atas, hal ini dapat memicu terjadinya peradangan dan penyumbatan pada lubang dan saluran system reproduksi.
              </p>
            </li>
            <li>
              <p class="un-indent">
                Dapat menimbulkan berbagai komplikasi seperti infeksi menular seksual, radang panggul serta kelahiran bayi prematur jika terjadi pada wanita hamil.
              </p>
            </li>
          </ol>

          <p>
            Keputihan abnormal yang terjadi pada wanita, selain berbahaya bagi kesehatan juga dapat mengalami gangguan pada saat hubungan seksual, hal ini disebabkan karena banyaknya pengeluaran lender yang berbau pada alat kelamin.Oleh karena itu, keputihan abnormal yang terjadi jangan anggap remeh, karana keputihan yang terjadi dapat mengancam kesehatan pada system reproduksi dan mengganggu kenyamanan, terlebih jika Anda sudah berkeluarga. Keputihan abnormal yang terjadi dapat mengganggu keharmonisan rumah tangga.
