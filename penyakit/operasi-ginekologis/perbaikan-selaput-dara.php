          <h3>Perbaikan Selaput Dara</h3>

          <br>

          <p>
            Perbaikan selaput dara dikenal dengan Hymenoplasty dimana prosedur untuk memulihkan selaput dara pada wanita untuk suatu tujuan seperti budaya dan lainnya. Dalam sebuah kebudayaan selaput dara dianggap sebagai tanda keperawanan. Hymenoplasty ini <b>DILAKUKAN ATAS ALASAN YANG JELAS</b>, perbaikan selaput dara biasanya dilakukan pada perempuan yang mengalami kekerasan atau pelecehan seksual seperti pemerkosaan, dan lain sebagainya yang sering menimbulkan beban psikologis terutama bagi salaput daranya robek tetapi blm pernah berhubungan seksual. Hilangnya keperawanan seorang wanita kadang bukan karena hubungan seksual, namun bisa terjadi karena kecelakaan saat beraktivitas, atau faktor tak sengaja lainnya. Kekuatan selaput dara itu sendiri sangat beragam. Ada yang sangat lemah sehingga bisa robek dengan mudah akibat aktivitas non-seksual. 
          </p>
          <p>
            Operasi selaput dara (Hymenoplasty) dilakukan untuk menyatukan kembali sisa-sisa selaput dara yang telah robek, operasi ini membutuhkan waktu yang cukup singkat. Prosedur ini dilakukan untuk merekatkan kembali selaput dara yang terdapat pada bibir alat kelamin wanita dengan menggunakan jahitan. Jahitan yang diterapkan adalah jenis jahitan yang larut atau dissolvable sehingga secara kasat mata tidak akan terlihat dan tidak perlu dilepas pasca operasi.
          </p>

