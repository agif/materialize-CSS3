          <h3>Bahaya Radang Panggul</h3>

          <br>

          <p>
            Radang panggul merupakan salah satu penyakit yang menyerang organ reproduksi wanita. penyakit ini dapat terjadi karena diakibatkan oleh adanya infeksi bakteri pada organ reproduksi wanita. Penyakit radang panggul juga disebut dengan Pelvic Inflammatory Disease (PID) dimana infeksi ini terjad pada organ reproduktif wanita seperti uterus, serviks, ovarium, atau tuba fallopi.
          </p>
          <p>
            Radang panggul dapat menyebabkan komplikasi serius jika tidak segera ditangani. Ketika penyakit radang panggul ini tidak segera ditangani atau penderita tidak menyelesaikan periode perawatan yang diwajibkan. Jenis komplikasi yang bisa timbul berupa nyeri panggul yang berkepanjangan (kronik), kemudian munculnya abses, berulangnya penyakit radang panggul pada penderita, kemandulan (infertilitas), dan terjadinya kehamilan ektopik.
          </p>

          <br>
