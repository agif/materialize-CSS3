            Selain itu infeksi radang panggul juga dapat menyebabkan luka dan menyempitnya tuba falopi hingga sel telur menjadi tersangkut kemudian berkembang di dalam tuba falopi. Jika kehamilan ektopik terus berlanjut maka hal ini dapat menyebabkan robekan pada bagian tuba fallopi sehinggaterjadi perdarahan di dalam yang mengancam nyawa penderitanya, sehingga untuk penanganan kondisi ini diperlukan tindakan operasi harus segera. Komplikasi kehamilan seperti keguguran, lahir prematur, dan kematian janin juga dapat terjadi jika penanganan tidak dilakukan hingga tuntas.
          </p>
