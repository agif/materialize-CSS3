          </p>
          <p>
            Bagi penderita yang mengalami radang panggul harus mengetahui jika penyakit ini berulang membuat kondisi organ reproduksi tersebut rentan terhadap bakteri. Inilah kenapa penderita radang panggul harus menyelesaikan masa perawatannya hingga tuntas demi mengurangi risiko terjadinya infertilitas serta nyeri panggul yang berkepanjangan dan sangat mengganggu aktivitas. Infeksi radang panggul yang berulang khususnya pada tuba falopi dapat mengakibatkan penderitanya mengalami kehamilan ektopik.
