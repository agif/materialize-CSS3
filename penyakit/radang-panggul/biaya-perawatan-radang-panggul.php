           	Radang panggul merupakan sebuah penyakit yang disebabkan oleh bakteri yang menyerang rahim,  tuba falopi, dan leher rahim. Infeksi yang terjadi akan menimbulkan sulitnya terjadi pembuahan yang sempurna sehingga keadaan radang panggul yang masih ringan terkadanga dapat diberikan penanganan sesuai jenis bakteri untuk mengatasi keadaan yang terjadi.Jika penyakit radang panggul sudah parah maka biaya yang dikeluarkan akan semakin banyak, oleh sebab itu sebaiknya lakukan pemeriksaan kepada dokter sedini mungkin.
