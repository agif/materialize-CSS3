            <h6><b>Diagnosis Radang Panggul</b></h6>

            <p>
              Diagnosis radang panggul atau pelvic inflammatory disease didapatkan berdasarkan gejala yang dialami pasien. Tetapi selain dengan melihat gejala salah satu prosedur yang digunakan untuk mengetahui apakah seseorang benar memiliki radang panggul adalah dengan cara mengambil sampel dari alat kelamin atau leher rahim. Pemeriksaan yang dilakukan pada organ reproduksi  akan memberikan informasi mengenai tingkat infeksi bakteri serta jenis bakteri yang menginfeksi. Beberapa tes yang mungkin dilakukan, antara lain tes urine, tes darah, tes kehamilan dan USG. Dokter juga dapat melakukan CT scan atau MRI.
            </p>
            <p>
              Pasien dapat didiagnosis radang panggul setelah hasil tes terhadap bakteri penyebab chlamydia atau gonore dinyatakan positif dan terbukti berisiko terhadap penyakit itu. Tetapi sebagian besar kasus hasil yang keluar adalah negatif bukan berarti pasien tersebut tidak memiliki risiko radang panggul.Selain itu dapat juga dilakukan sebuah tes berupa tindakan operasi laparoskopi (keyhole surgery) atau pembedahan kecil pada bagian perut mungkin dilakukan untuk memasukkan kamera kecil dan melihat organ perut pasien.
            </p>

