          <h3>Penanganan Radang Panggul</h3>

          <br>

          <p>
            Jika Anda mengalami radang panggul, tetapi binggung bagaimana cara penanganan penyakit ini. Sebaiknya Anda tidak mencoba penanganan sendiri tanpa berkonsultasi langsung pada dokter, karena yang di khawatirkan jika salah penanganan akan semakin memperparah kondisi yang Anda alami saat ini. Sebaiknya segera konsultasikan diri Anda pada dokter untuk mengatasi kondisi yang Anda alami saat ini.
          </p>
          <p>
            Penanganan untuk penyakit radang panggul berbeda-beda pada setia pasien tergantung dengan penyebab dan tingkat keparahan, Jika PID didiagnosis secara dini, maka dapat diatasi dengan baik. Tetapi jika dibiarkan terus menerus, penanganan tidak akan memperbaiki kerusakan yang telah terjadi pada sistem reproduksi. Jadi, semakin lama radang panggul tidak diatasi maka semakin besar kemungkinan seseorang memiliki komplikasi.
          </p>
          <p>
            Penanganan dini sangatlah baik sebelum infeksi ini menyebar. Dokter mungkin akan memberi resep yang diberikan melalui suntikan atau mulut. Pada infeksi yang lebih parah hal pertama yang dibutuhkan adalah rawat inap di rumah sakit dan antibiotik akan diberikan melalui infus. Untuk nanah yang berada di sekitar tuba fallopi atau ovarium yang kondisinya tidak menjadi lebih baik dengan antibiotik, dokter akan melakukan operasi biasanya operasi laparaskopi untuk mengeluarkan nanah. Selain itu pasangan seks Anda perlu melakukan penanganan jika dia memiliki penyakit tersebut. Anda perlu melakukan seks secara aman seperti dengan menggunakan kondomuntuk mencegah penularan.
          </p>

