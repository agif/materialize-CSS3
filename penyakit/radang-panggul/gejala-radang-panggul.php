          <h6><b>Gejala Yang Kemungkinan Terjadi Pada Radang Panggul (PID)</b></h6>

          <p>
            Penyakit radang panggul (PID)hanya menyebabkan tanda dan gejala yang ringan atau tidak sama sekali. Jika gejalanya berat, maka penyakit radang panggul (PID) dapat menyebabkan demam, menggigil, nyeri perut bagian bawah atau panggul (pelvic) yang parah terutama selama dalam pemeriksaan panggul (pelvic) dan terjadi ketidaknyamanan pada usus.
          </p>

          <br>
          <img class="responsive-img materialboxed" src="penyakit/radang-panggul/thumbnail/Gejala-Radang-Panggul.jpg">
          <br>

          <p>
