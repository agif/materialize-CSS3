          <h6><b>7 Gejala Gatal Pada Alat Kelamin Wanita Meliputi:</b></h6>

          <ol class="browser-default">
            <li><b>Gejala Infeksi Jamur Pada Alat Kelamin Wanita</b>
              <ul>
                <li>Gatal pada alat kelamin wanita</li>
                <li>Terjadi pembengkakan disekitar alat kelamin wanita</li>
                <li>Terasa terbakar saat buang air kecil (BAK atau kencing) atau berhubungan seksual</li>
                <li>Nyeri saat berhubungan seksual</li>
                <li>Adanya rasa sakit</li>
                <li>Kemerahan</li>
                <li>Adanya ruam</li>
              </ul>
            </li>
            <li><b>Gejala Vulvovaginitis</b>
              <ul>
                <li>Iritasi pada daerah genital</li>
                <li>Adanya rasa gatal</li>
                <li>Peradangan disekitar labia (bibir alat kelamin wanita) dan daerah perineum</li>
                <li>Keputihan yang meningkat dan berbau sangat tajam</li>
                <li>Ketidaknyamanan saat buang air kecil (BAK atau kencing)</li>
              </ul>
            </li>
            <li><b>Gejala Atrofi Pada Alat Kelamin Wanita</b>
              <ul>
                <li>Penipisan dinding alat kelamin wanita</li>
                <li>Memendeknya dan mengencangnya saluran alat kelamin wanita</li>
                <li>Berkurangnya kelembaban alat kelamin wanita (kekeringan alat kelamin wanita)</li>
                <li>Pembakaran alat kelamin wanita (peradangan)</li>
                <li>Bercak setelah berhubungan seksual</li>
                <li>Ketidaknyamanan atau adanya rasa sakit selama berhubungan seksual</li>
                <li>Sakit atau seperti terbakar saat buang air kecil (BAK atau kencing)</li>
                <li>Infeksi saluran kemih (ISK) yang lebih sering</li>
                <li>Inkontinensia urine (kebocoran yang tidak disengaja)</li>
              </ul>
            </li>
            <li><b>Gejala Trikomoniasis</b>
              <ul>
                <li>Keputihan yang bisa berwarna putih, abu-abu, kuning atau hijau dan biasanya berbusa dengan bau yang tidak enak</li>
                <li>Adanya bercak pada alat kelamin wanita atau perdarahan</li>
                <li>Pembakaran genital atau adanya rasa gatal</li>
                <li>Kemerahan genital atau pembengkakan</li>
                <li>Sering buang air kecil (urgensi kencing)</li>
                <li>Nyeri saat buang air kecil  atau saar hubungan seksual</li>
                <li>Terasa terbakar saat buang air kecil atau setelah ejakulasi</li>
              </ul>
            </li>
            <li><b>Gejala Infeksi Cacing Kremi</b>
              <ul>
                <li>Rasa gatal yang sering dan sangat kuat dari area anus</li>
                <li>Tidur yang gelisah karena adanya rasa gatal dan ketidaknyamanan di area anus</li>
                <li>Nyeri dan adanya ruam atau iritasi kulit lainnya disekitar anus</li>
                <li>Keberadaan cacaing kremi di area anus</li>
                <li>Keberadaan cacing kremi di tinja</li>
              </ul>
            </li>
            <li><b>Gejala Urethritis</b>
              <ul>
                <li><u>Gejala Pada Pria</u></i>
                  <ul>
                    <li>Sensasi terbakar saat buang air kecil (BAK atau kencing)</li>
                    <li>Rasa gatal atau terbakar didekat alat kelamin pria</li>
                    <li>Adanya darah di air mani atau urine</li>
                  </ul>
                </li>
                <li><u>Gejala Pada Wanita</u>
                  <ul>
                    <li>Seringnya buang air kecil (urgensi kencing)</li>
                    <li>Ketidaknyamanan saat buang air kecil (BAK atau kencing)</li>
                    <li>Terasa terbakar atau iritasi pada uretra (saluran yang menghubungkan kantung kemih ke lingkungan luar tubuh)</li>
                  </ul>
                </li>
              </ul>
            </li>
            <li><b>Gejala Kutil Kelamin</b>
              <ul>
                <li>Terjadinya keputihan</li>
                <li>Terasa gatal</li>
                <li>Perdarahan</li>
                <li>Terasa terbakar</li>
              </ul>
            </li>
          </ol>

          <p>
            Jika kutil kelamin menyebar maka kondisinya mengakibatkan ketidaknyamanan dan sangat menyakitkan. Kutil kelamin pada pria bisa tumbuh di alat kelamin pria, skrotum, paha dan didalam atau disekitar anus. Sedangkan kutil kelamin pada wanita bisa tumbuh di dalam alat kelamin wanita dan di anus, diluar alat kelamin wanita dan di anus dan di serviks (leher rahim).
          </p>

